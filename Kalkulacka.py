
class Kalkulacka:
    def __init__(self, cislo1, cislo2):
        self.cislo1 = cislo1
        self.cislo2 = cislo2

    def scitani(self):
           
        return self.cislo1 + self.cislo2
        

    def odcitani(self):
        return self.cislo1 - self.cislo2
    
    def nasobeni(self):
        return self.cislo1 * self.cislo2
    
    def deleni(self):
        return self.cislo1 / self.cislo2
        

    



    

